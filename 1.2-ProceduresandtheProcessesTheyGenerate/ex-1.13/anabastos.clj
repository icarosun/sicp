; Seja ψ = (1 - √5)/2.
; E φ = (1 + √5)/2 pois é a goden ratio

; Fib(n) = (φ^n - ψ^n) / √5

; Fib(0) = (φ^0 - ψ^0)/sqrt(5) = 0
; Fib(1) = (φ^1 - ψ^1)/sqrt(5) = 1
; Fib(2) = Fib(0) + Fib(1) = 1


; Para n = 0
; (φn - ψn) / √5 = (φ0 - ψ0) / √5
; = (1 - 1) / √5
; = 0 / √5
; = 0

; Para n = 1
; (φn - ψn) / √5 = (φ1 - ψ1) / √5
; = ((1 + √5)/2) - ((1 - √5)/2) / √5
; = (1/2 + √5/2) - (1/2 - √5/2) / √5
; = (1/2 + √5/2 - 1/2 + √5/2) / √5
; = (√5/2 + √5/2) / √5
; = (2 * √5/2) / √5
; = √5 / √5
; = 1

; Para n = 2
; (φn - ψn) / √5 = (φ2 - ψ2) / √5
; = ((φ + 1) - (ψ + 1)) / √5
; = (((1 + √5)/2 + 1) - ((1 - √5)/2 + 1)) / √5
; = ((1 + √5)/2 + 1 - (1 - √5)/2 - 1) / √5
; = ((1 + √5)/2 - (1 - √5)/2) / √5
; = ((1 + √5) - (1 - √5)) / 2 / √5
; = (1 + √5 - 1 + √5) / 2 / √5
; = (√5 + √5) / 2 / √5
; = (2 * √5) / 2 / √5
; = √5 / √5
; = 1

